# Task 1: Perform Basic Mathematical Operations

a = int(input("ENTER FIRST NUMBER: "))
b = int(input("ENTER SECOND NUMBER: "))

print("ADDITION", a + b)
print("SUBTRACTION", a - b)
print("MULTIPLICATION", a * b)

if b != 0:
    print("DIVISION", a / b)
else:
    print("DIVISION not possible")
